function escapeForSrcdoc(html) {
  return html.replace(/&/g, '&amp;').replace(/"/g, '&quot;');
}

function updateGeneratorForm(lang) {
  const formWrapper = document.getElementById('form-wrapper');
  if (!formWrapper) return;
  const mode = localStorage.getItem('routeGeneratorMode') || 'server';
  if (mode === 'local') {
    renderLocalRoute(lang);
  } else {
    let iframeSrc = 'https://script.google.com/macros/s/AKfycbw0X0_uO0LEeQnf-hmN7i8OYK_AnDQhOy-7o4lP3cj5jIzWHFvKuqrPM9ytGhk4qf7f0w/exec';
    if (lang === 'en') {
      iframeSrc = 'https://script.google.com/macros/s/AKfycby1ZpI8gGA1g0F19opmVWR1T6VRoYRnYVmmTUvW9ilKOQHLsTkZPQBymqS9qoGjkQkp/exec';
    }
    formWrapper.innerHTML = `<iframe src="${iframeSrc}" style="width:100%;min-height:900px;height:900px;border:none;display:block;" frameborder="0"></iframe>`;
  }
}

function renderLocalRoute(lang) {
  const formWrapper = document.getElementById('form-wrapper');
  if (!formWrapper) return;
  const l = lang || localStorage.getItem('language') || 'ru';
  formWrapper.innerHTML = `
    <div style="width:100%;min-height:100vh;">
      <iframe
        id="route-frame"
        srcdoc="${escapeForSrcdoc(getRouteEditorHTML(l))}"
        style="width:100%;min-height:900px;height:900px;border:none;display:block;"
        sandbox="allow-scripts allow-forms allow-same-origin allow-modals allow-downloads"
      ></iframe>
    </div>`;
}

