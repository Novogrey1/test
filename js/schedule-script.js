function generateSchedule() {
  const lang = getCurrentLanguage();
  const route = document.getElementById("route").value;
  const startTimeInput = document.getElementById("start-time").value;

  if (!startTimeInput) {
    alert(lang === 'en' ? "Enter departure time!" : "Введите время отправления!");
    return;
  }

  const stops = routes[route];
  let [hours, minutes] = startTimeInput.split(":").map(Number);

  const mskOffset = 3;
  const localOffset = new Date().getTimezoneOffset() / 60;
  hours -= (mskOffset + localOffset);
  if (hours >= 24) hours -= 24;
  else if (hours < 0) hours += 24;

  const roundLabel = translations[lang]["Круг"] || "Круг";

  let tableHTML = `<table class="schedule-table"><thead><tr><th>${roundLabel}</th>`;
  stops.forEach(stop => {
    tableHTML += `<th><span class="tooltip-wrap">${stop.name}<span class="tooltip-text">${stop.fullName}</span></span></th>`;
  });
  tableHTML += '</tr></thead><tbody>';

  for (let round = 1; round <= 3; round++) {
    tableHTML += `<tr><td><strong>${round}</strong></td>`;
    stops.forEach(stop => {
      minutes += stop.duration;
      if (minutes >= 60) {
        hours += Math.floor(minutes / 60);
        minutes %= 60;
      }
      if (hours >= 24) hours -= 24;
      const timeString = `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}`;
      tableHTML += `<td data-h="${hours}" data-m="${minutes}">${timeString}</td>`;
    });
    tableHTML += '</tr>';
  }
  tableHTML += '</tbody></table>';

  document.getElementById("schedule").innerHTML = tableHTML;

  const [startH, startM] = startTimeInput.split(":").map(Number);
  const scheduleTitle = translations[lang]["Расписание"] || "Расписание";
  const code = `№${route}.${String(startH).padStart(2, '0')}.${String(startM).padStart(2, '0')}`;
  document.getElementById("schedule-code").textContent = `${scheduleTitle} ${code}`;

  const legendText = legends[route].join("; ");
  const legendLabel = lang === 'en' ? "<strong>Stop abbreviations:</strong> " : "<strong>Расшифровка остановок:</strong> ";
  document.getElementById("legend").innerHTML = legendLabel + legendText;

  document.getElementById("schedule-output").style.display = "block";
  highlightCells();
}

function generateScheduleFromCode() {
  const lang = getCurrentLanguage();
  const input = document.getElementById("schedule-code-input").value;
  const parts = input.replace('№', '').split('.');

  if (parts.length !== 3) {
    alert(lang === 'en' ? "Invalid schedule code!" : "Неверный код расписания!");
    return;
  }

  const [route, hours, minutes] = parts;
  if (!routes[route] || isNaN(Number(hours)) || isNaN(Number(minutes))) {
    alert(lang === 'en' ? "Invalid schedule code!" : "Неверный код расписания!");
    return;
  }

  document.getElementById("start-time").value = `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}`;
  document.getElementById("route").value = route;
  generateSchedule();
}

function highlightCells() {
  const now = new Date();
  const currentTotal = now.getHours() * 60 + now.getMinutes();

  document.querySelectorAll(".schedule-table tbody td[data-h]").forEach(cell => {
    const h = parseInt(cell.getAttribute("data-h"));
    const m = parseInt(cell.getAttribute("data-m"));
    const diff = (h * 60 + m) - currentTotal;
    if (diff <= 1 && diff >= -1) {
      cell.classList.add("highlight");
    } else {
      cell.classList.remove("highlight");
    }
  });

  setTimeout(highlightCells, 1000);
}

