function initLanguageSystem() {
    if (!originalHTML) {
        originalHTML = document.body.innerHTML;
    }

    const savedLanguage = localStorage.getItem('language') || 'ru';
    setLanguage(savedLanguage, true);
    updateLangButton(savedLanguage);
    setupLanguageButton();
    reinitializeEventListeners();
    initThemeSystem();
}

function setLanguage(lang, skipAnim = false) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;

    const EXIT_SEL = '.hero-events-content h1, .section-header, .calendar-card, .form-card, .contacts-section h2, .docs-section h2, .disclaimer';

    if (!skipAnim) {
        const exitEls = Array.from(document.querySelectorAll(EXIT_SEL));
        exitEls.forEach((el, i) => {
            el.style.animationDelay = (i * 30) + 'ms';
            el.classList.remove('lang-enter');
            el.classList.add('lang-exit');
        });
    }

    const exitDuration = skipAnim ? 0 : Math.min(document.querySelectorAll(EXIT_SEL).length * 30 + 200, 400);

    setTimeout(() => {
        document.body.innerHTML = originalHTML;

        function translateNode(node) {
            if (node.nodeType === 3) {
                const originalText = node.textContent;
                const trimmedText = originalText.trim();
                if (trimmedText && translations[lang] && translations[lang][trimmedText]) {
                    const leadingSpace = originalText.match(/^\s*/)[0];
                    const trailingSpace = originalText.match(/\s*$/)[0];
                    node.textContent = leadingSpace + translations[lang][trimmedText] + trailingSpace;
                }
            } else if (node.nodeType === 1 && node.tagName !== 'SCRIPT' && node.tagName !== 'STYLE') {
                Array.from(node.childNodes).forEach(child => {
                    if (node.contains(child)) translateNode(child);
                });
            }
        }
        Array.from(document.body.childNodes).forEach(child => {
            if (document.body.contains(child)) translateNode(child);
        });

        reinitializeEventListeners();
        reinitializeTheme();
        initMobileMenu();
        updateLangButton(lang);
        document.body.classList.add('loaded');

        requestAnimationFrame(() => requestAnimationFrame(() => {
            if (!skipAnim) {
                const enterEls = Array.from(document.querySelectorAll(EXIT_SEL));
                enterEls.forEach((el, i) => {
                    el.style.animationDelay = (i * 45) + 'ms';
                    el.classList.add('lang-enter');
                    el.addEventListener('animationend', () => {
                        el.classList.remove('lang-enter');
                        el.style.animationDelay = '';
                    }, { once: true });
                });
            }
            const heroContent = document.querySelector('.hero-events-content');
            if (heroContent) heroContent.classList.add('animate');
            initScrollReveal();
        }));
    }, exitDuration);
}

function updateLangButton(lang) {
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
        langBtn.textContent = lang === 'ru' ? 'EN' : 'RU';
    }
}

function setupLanguageButton() {
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
        const newLangBtn = langBtn.cloneNode(true);
        langBtn.parentNode.replaceChild(newLangBtn, langBtn);

        document.getElementById('lang-btn').addEventListener('click', function () {
            const currentLang = localStorage.getItem('language') || 'ru';
            const newLang = currentLang === 'ru' ? 'en' : 'ru';
            setLanguage(newLang);
            updateLangButton(newLang);
        });
    }
}

function reinitializeEventListeners() {
    setupLanguageButton();

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({ behavior: 'smooth', block: 'start' });
            }
        });
    });
    
    initDropdowns();
    initMobileMenu();
}

function reinitializeTheme() {
    const savedTheme = localStorage.getItem('theme') || 'light';
    applyTheme(savedTheme);

    const themeBtn = document.getElementById('theme-btn');
    if (themeBtn) {
        themeBtn.removeEventListener('click', handleThemeClick);
        themeBtn.addEventListener('click', handleThemeClick);
    }
    
    initDropdowns();
    initMobileMenu();
}

