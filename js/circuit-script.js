function updateBannerState() {
  const banner   = document.getElementById('error-banner');
  const btnLocal = document.getElementById('switch-local-btn');
  const btnServer = document.getElementById('switch-server-btn');
  const titleEl  = document.getElementById('banner-title');
  const descEl   = document.getElementById('banner-desc');
  if (!banner) return;

  if (btnLocal)  btnLocal.textContent  = t('banner-btn-local');
  if (btnServer) btnServer.textContent = t('banner-btn-server');
  if (descEl)    descEl.textContent    = t('banner-desc');

  const mode = localStorage.getItem('circuitGeneratorMode') || 'server';
  if (mode === 'local') {
    banner.classList.add('error-banner--local');
    if (titleEl) titleEl.textContent = t('banner-title-local');
    if (btnLocal)  btnLocal.style.display = 'none';
    if (btnServer) btnServer.style.display = '';
  } else {
    banner.classList.remove('error-banner--local');
    if (titleEl) titleEl.textContent = t('banner-title');
    if (btnLocal)  btnLocal.style.display = '';
    if (btnServer) btnServer.style.display = 'none';
  }

  if (localStorage.getItem('circuitBannerCollapsed') === 'true') {
    banner.classList.add('collapsed');
  } else {
    banner.classList.remove('collapsed');
  }
}

// ИСПРАВЛЕНО: прямые onclick-обработчики, переназначаются после каждой замены DOM
function initBanner() {
  updateBannerState();

  const toggleBtn = document.getElementById('error-banner-toggle');
  const localBtn  = document.getElementById('switch-local-btn');
  const serverBtn = document.getElementById('switch-server-btn');

  if (toggleBtn) {
    toggleBtn.onclick = function (e) {
      e.stopPropagation();
      const banner = document.getElementById('error-banner');
      if (!banner) return;
      banner.classList.toggle('collapsed');
      localStorage.setItem('circuitBannerCollapsed',
        banner.classList.contains('collapsed') ? 'true' : 'false');
    };
  }

  if (localBtn) {
    localBtn.onclick = function (e) {
      e.stopPropagation();
      localStorage.setItem('circuitGeneratorMode', 'local');
      localStorage.setItem('circuitBannerCollapsed', 'false');
      updateBannerState();
      renderLocalCircuit(getCurrentLanguage());
    };
  }

  if (serverBtn) {
    serverBtn.onclick = function (e) {
      e.stopPropagation();
      localStorage.setItem('circuitGeneratorMode', 'server');
      updateBannerState();
      updateGeneratorForm(getCurrentLanguage());
    };
  }
}

