function cg(g,depth){
  if(depth>40)return gOut[g.id]||[false];
  const d=GDEFS[g.type];
  if(d.interactive)return[g.state];
  const ins=[];
  for(let i=0;i<d.inputs;i++){
    const w=wires.find(w=>w.tGate===g.id&&w.tPort===i);
    if(!w){ins.push(false);continue;}
    const s=gates.find(x=>x.id===w.fGate);
    ins.push(s?(cg(s,depth+1))[w.fPort]||false:false);
  }
  const a=ins[0]||false,b=ins[1]||false;
  switch(g.type){
    case'AND':return[a&&b];case'NAND':return[!(a&&b)];case'OR':return[a||b];
    case'NOR':return[!(a||b)];case'NOT':return[!a];case'XOR':return[a!==b];case'XNOR':return[a===b];
    case'DELAY':case'SUSTAIN':return[a];
    case'SR_LATCH':{const p=gOut[g.id]||[false];return a&&!b?[true]:!a&&b?[false]:p;}
    case'T_LATCH':{const p=gOut[g.id]||[false];return a&&!g._tPrev?[!p[0]]:p;}
    case'OSCILLATOR':return[g._osc||false];
    case'TIME_TRIGGER':{
      const fr=parseHHMM(g.cfgFrom||'00:00'),to=parseHHMM(g.cfgTo||'00:00');
      const cur=(simStartSec+simTime)%86400;
      return[fr<=to?(cur>=fr&&cur<to):(cur>=fr||cur<to)];
    }
    case'LAMP':return[a];default:return[false];
  }
}
