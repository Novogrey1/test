function updateLangButton(lang) {
  const langBtn = document.getElementById('lang-btn');
  if (langBtn) langBtn.textContent = lang === 'ru' ? 'EN' : 'RU';
}

function setLanguage(lang) {
  localStorage.setItem('language', lang);
  document.documentElement.lang = lang;

  // Восстанавливаем оригинальный HTML и переводим
  document.body.innerHTML = originalHTML;

  function translateNode(node) {
    if (node.nodeType === 3) {
      const text = node.textContent.trim();
      if (text && translations[lang] && translations[lang][text]) {
        node.textContent = node.textContent.replace(text, translations[lang][text]);
      }
    } else if (node.nodeType === 1 && node.tagName !== 'SCRIPT') {
      for (let i = 0; i < node.childNodes.length; i++) {
        translateNode(node.childNodes[i]);
      }
    }
  }
  for (let i = 0; i < document.body.childNodes.length; i++) {
    translateNode(document.body.childNodes[i]);
  }

  // Переинициализируем всё после замены DOM
  initThemeSystem();
  initMobileMenu();
  initDropdowns();
  initBanner();
  updateLangButton(lang);
  updateGeneratorForm(lang);
  updateLogicReference(lang);

  // Назначаем кнопку языка
  const langBtn = document.getElementById('lang-btn');
  if (langBtn) {
    langBtn.onclick = function () {
      const newLang = getCurrentLanguage() === 'ru' ? 'en' : 'ru';
      setLanguage(newLang);
    };
  }
}

function initLanguageSystem() {
  if (!originalHTML) {
    originalHTML = document.body.innerHTML;
  }
  const lang = getCurrentLanguage();
  setLanguage(lang);
}

