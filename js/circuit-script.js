function distToWire(w,px,py){
  const fg=gates.find(g=>g.id===w.fGate),tg=gates.find(g=>g.id===w.tGate);
  if(!fg||!tg)return Infinity;
  const fp=pp(fg,'out',w.fPort),tp=pp(tg,'in',w.tPort);
  const cx=(fp.x+tp.x)/2;
  let minD=Infinity;
  for(let i=0;i<=30;i++){
    const t=i/30,mt=1-t;
    const x=mt*mt*mt*fp.x+3*mt*mt*t*cx+3*mt*t*t*cx+t*t*t*tp.x;
    const y=mt*mt*mt*fp.y+3*mt*mt*t*fp.y+3*mt*t*t*tp.y+t*t*t*tp.y;
    const d=Math.hypot(x-px,y-py);
    if(d<minD)minD=d;
  }
  return minD;
}
