function updateGeneratorForm(lang) {
  const formWrapper = document.getElementById('form-wrapper');
  if (!formWrapper) return;
  const mode = localStorage.getItem('announcementGeneratorMode') || 'server';
  if (mode === 'local') {
    renderLocalAnnouncement(lang);
    return;
  }

  let iframeSrc = "https://script.google.com/macros/s/AKfycby0yF_RA9qxS7yjr0CqqQ0-Ln55OYlgEmctY9BY3w9T93yhl5VcmRETHtgQswMByIVXaA/exec";
  if (lang === 'en') {
    iframeSrc = "https://script.google.com/macros/s/AKfycbzTpBcYlFKXGaAhB5AFW0L5OCq7u_uJ680ii2MB8ljWXy_sI88b-WO0ZbOaIYpaI7ffzg/exec";
  }
  formWrapper.innerHTML = `<iframe class="announcement-frame announcement-frame--server" src="${iframeSrc}" frameborder="0" marginheight="0" marginwidth="0" style="width:100%;min-height:2600px;height:2600px;border:none;display:block;"></iframe>`;
}

function getLocalAnnouncementFrameSrc(lang) {
  const currentLang = lang || (localStorage.getItem('language') || 'ru');
  return `local-generator.html?lang=${encodeURIComponent(currentLang)}`;
}

function renderLocalAnnouncement(lang) {
  const formWrapper = document.getElementById('form-wrapper');
  if (!formWrapper) return;
  const theme = localStorage.getItem('theme') || 'light';
  const iframeBackground = theme === 'dark' ? '#0a0a0a' : '#f3f4f6';
  formWrapper.style.backgroundColor = iframeBackground;
  formWrapper.innerHTML = `
    <div style="width:100%;min-height:100vh;background:${iframeBackground};">
      <iframe
        id="announcement-local-frame"
        class="announcement-frame announcement-frame--local"
        src="${getLocalAnnouncementFrameSrc(lang)}"
        title="Announcement local generator"
        style="width:100%;min-height:1200px;height:1200px;border:none;display:block;background:${iframeBackground};"
        sandbox="allow-scripts allow-forms allow-same-origin allow-modals allow-downloads"
      ></iframe>
    </div>`;
}

