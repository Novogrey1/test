let originalHTML = null;

// ============================================
// THEME SYSTEM
// ============================================

function initThemeSystem() {
  const savedTheme = localStorage.getItem('theme') || 'light';
  applyTheme(savedTheme);
  const themeBtn = document.getElementById('theme-btn');
  if (themeBtn) {
    themeBtn.addEventListener('click', handleThemeClick);
  }
}

function applyTheme(theme) {
  localStorage.setItem('theme', theme);
  const themeBtn = document.getElementById('theme-btn');
  if (theme === 'dark') {
    document.body.classList.add('dark-mode');
    if (themeBtn) themeBtn.textContent = '☀️';
  } else {
    document.body.classList.remove('dark-mode');
    if (themeBtn) themeBtn.textContent = '🌙';
  }
}

function handleThemeClick() {
  const currentTheme = localStorage.getItem('theme') || 'light';
  applyTheme(currentTheme === 'light' ? 'dark' : 'light');
}

// ============================================
// LANGUAGE SYSTEM
// ============================================

function initLanguageSystem() {
  if (!originalHTML) {
    originalHTML = document.body.innerHTML;
  }
  const savedLanguage = localStorage.getItem('language') || 'ru';
  setLanguage(savedLanguage);
  updateLangButton(savedLanguage);
  setupLanguageButton();
  reinitializeEventListeners();
  initThemeSystem();
}

function setLanguage(lang) {
  localStorage.setItem('language', lang);
  document.documentElement.lang = lang;
  document.body.innerHTML = originalHTML;

  // data-i18n: replace first text node (preserves child elements like arrows, spans)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!translations[lang] || !translations[lang][key]) return;
    const translated = translations[lang][key];
    const childNodes = Array.from(el.childNodes);
    const hasElementChildren = childNodes.some(n => n.nodeType === 1);
    if (!hasElementChildren) {
      el.textContent = translated;
    } else {
      const firstText = childNodes.find(n => n.nodeType === 3);
      if (firstText) {
        const orig = firstText.textContent;
        const trailingWS = /\s+$/.test(orig) ? orig.match(/\s+$/)[0] : '';
        const translatedEndsWS = /\s$/.test(translated);
        firstText.textContent = translated + (translatedEndsWS ? '' : trailingWS);
      } else {
        el.insertBefore(document.createTextNode(translated), el.firstChild);
      }
    }
  });

  // data-i18n-html: replace innerHTML (for rich content with tags)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // data-i18n-placeholder: replace placeholder attribute
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  reinitializeEventListeners();
  reinitializeTheme();
  initMobileMenu();
  initEditorListeners();
}

