function applyTheme(theme) {
    localStorage.setItem('theme', theme);
    const themeBtn = document.getElementById('theme-btn');
    
    if (theme === 'dark') {
        document.body.classList.add('dark-mode');
        if (themeBtn) themeBtn.textContent = '☀️';
    } else {
        document.body.classList.remove('dark-mode');
        if (themeBtn) themeBtn.textContent = '🌙';
    }
}



async function updateServerButton() {
    try {
        const response = await fetch('https://script.google.com/macros/s/AKfycbylGfVRtvzZI2RMhidNIDhCChNFyFeH7BLyc-PZxTNB35ImcTebTddW1hwq2V2ef5__zw/exec');
        const data = await response.json();
        const status = data.status;

        const serverButton = document.getElementById('server-button');
        if (!serverButton) return;

        if (status === 'Disable RP Server' || status === 'Disable Non-RP Server' || status === 'Disable Shift Server') {
            serverButton.classList.add('red');
            serverButton.classList.remove('green', 'yellow');
        } else if (status === 'Включить RP сервер' || status === 'Включить Non-RP сервер' || status === 'Включить сервер для смен') {
            serverButton.classList.add('green');
            serverButton.classList.remove('red', 'yellow');
        } else if (status === 'Перезагрузка / Restarting') {
            serverButton.classList.add('yellow');
            serverButton.classList.remove('red', 'green');
        }
    } catch (error) {
        console.error('Ошибка при обновлении кнопки сервера:', error);
    }
}

