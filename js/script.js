// Функция переинициализации обработчиков
function reinitializeEventListeners() {
    setupLanguageButton();
    
    // Плавная прокрутка
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });
    
    initDropdowns();
    initMobileMenu();
}

// Переинициализируем тему после смены языка
function reinitializeTheme() {
    const savedTheme = localStorage.getItem('theme') || 'light';
    applyTheme(savedTheme);
    
    const themeBtn = document.getElementById('theme-btn');
    if (themeBtn) {
        themeBtn.removeEventListener('click', handleThemeClick);
        themeBtn.addEventListener('click', handleThemeClick);
    }
    initDropdowns();
}

// Обработчик кнопки темы
function handleThemeClick() {
    const themeBtn = document.getElementById('theme-btn');
    if (themeBtn) {
        themeBtn.classList.remove('spinning');
        void themeBtn.offsetWidth; // force reflow to restart animation
        themeBtn.classList.add('spinning');
        themeBtn.addEventListener('animationend', () => {
            themeBtn.classList.remove('spinning');
        }, { once: true });
    }
    const currentTheme = localStorage.getItem('theme') || 'light';
    const newTheme = currentTheme === 'light' ? 'dark' : 'light';
    applyTheme(newTheme);
}

