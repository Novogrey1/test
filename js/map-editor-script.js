            const shouldAnimate = !skipAnim && initialLanguagePaintDone && !reduceMotion;

            if (!shouldAnimate) {
                const result = originalSetLanguage.call(this, lang, true);
                initialLanguagePaintDone = true;
                afterPaint(refresh);
                return result;
            }

