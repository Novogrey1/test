function initLanguageSystem() {
    if (!originalHTML) originalHTML = document.body.innerHTML;
    const savedLanguage = localStorage.getItem('language') || 'ru';
    setLanguage(savedLanguage);
    updateLangButton(savedLanguage);
    setupLanguageButton();
    reinitializeEventListeners();
    initThemeSystem();
}

function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;
    document.body.innerHTML = originalHTML;

    // Translate short nav/footer text nodes via dictionary
    function translateNode(node) {
        if (node.nodeType === 3) {
            let text = node.textContent.trim();
            if (text && translations[lang] && translations[lang][text]) {
                node.textContent = node.textContent.replace(text, translations[lang][text]);
            }
        } else if (node.nodeType === 1 && node.tagName !== 'SCRIPT') {
            for (let i = 0; i < node.childNodes.length; i++) translateNode(node.childNodes[i]);
        }
    }
    for (let i = 0; i < document.body.childNodes.length; i++) translateNode(document.body.childNodes[i]);

    // Swap content of elements that carry data-en attribute (command descriptions, headings, notes)
    if (lang === 'en') {
        document.querySelectorAll('[data-en]').forEach(function (el) {
            // Skip code-toggle-btn because its data-en contains the label text + arrow together
            // We handle those separately below
            if (el.classList.contains('code-toggle-btn')) return;
            el.innerHTML = el.getAttribute('data-en');
        });
        // For code-toggle-btn: set text content preserving the arrow span
        document.querySelectorAll('.code-toggle-btn[data-en]').forEach(function (btn) {
            var arrowSpan = document.createElement('span');
            arrowSpan.className = 'code-arrow';
            arrowSpan.textContent = '▾';
            btn.innerHTML = '';
            btn.appendChild(document.createTextNode(btn.getAttribute('data-en').replace(' ▾', '') + ' '));
            btn.appendChild(arrowSpan);
        });
    }

    reinitializeEventListeners();
    reinitializeTheme();
    initMobileMenu();
    initCodeToggles();
    if (typeof window._searchReinit === 'function') window._searchReinit();
}

function updateLangButton(lang) {
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) langBtn.textContent = lang === 'ru' ? 'EN' : 'RU';
}

function setupLanguageButton() {
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) {
        const newBtn = langBtn.cloneNode(true);
        langBtn.parentNode.replaceChild(newBtn, langBtn);
        document.getElementById('lang-btn').addEventListener('click', function () {
            const currentLang = localStorage.getItem('language') || 'ru';
            const newLang = currentLang === 'ru' ? 'en' : 'ru';
            setLanguage(newLang);
            updateLangButton(newLang);
        });
    }
}

function reinitializeEventListeners() { setupLanguageButton(); initDropdowns(); initMobileMenu(); }

function reinitializeTheme() {
    const savedTheme = localStorage.getItem('theme') || 'light';
    applyTheme(savedTheme);
    const themeBtn = document.getElementById('theme-btn');
    if (themeBtn) { themeBtn.removeEventListener('click', handleThemeClick); themeBtn.addEventListener('click', handleThemeClick); }
    initDropdowns(); initMobileMenu();
}

