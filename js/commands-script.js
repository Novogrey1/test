// Fuzzy score: returns 0–1, higher = better match
// Only substring and word-level — NO character-subsequence (too loose)
function fuzzyScore(text, query) {
    text = text.toLowerCase();
    query = query.toLowerCase().trim();
    if (!query) return 1;

    // Exact phrase
    if (text.includes(query)) return 1;

    // All query words present as substrings
    const words = query.split(/\s+/).filter(Boolean);
    if (words.length > 1) {
        const allPresent = words.every(w => text.includes(w));
        if (allPresent) return 0.9;

        // At least half the words match
        const matched = words.filter(w => text.includes(w)).length;
        if (matched >= Math.ceil(words.length / 2)) return 0.6;
    }

    return 0;
}

// Highlight all occurrences of query words in innerHTML (plain text nodes only)
function highlightText(html, query) {
    if (!query.trim()) return html;
    const words = [...new Set(query.trim().toLowerCase().split(/\s+/))].filter(Boolean);
    let result = html;
    words.forEach(word => {
        const escaped = word.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
        const re = new RegExp(`(${escaped})`, 'gi');
        // Only highlight outside HTML tags
        result = result.replace(/(<[^>]+>)|([^<]+)/g, (m, tag, txt) => {
            if (tag) return tag;
            return txt.replace(re, '<mark class="cmd-hl">$1</mark>');
        });
    });
    return result;
}

function initSearch() {
    const input = document.getElementById('cmd-search');
    const clearBtn = document.getElementById('cmd-search-clear');
    const resultsEl = document.getElementById('cmd-search-results');
    const noResults = document.getElementById('cmd-no-results');
    const noResultsText = document.getElementById('cmd-no-results-text');
    if (!input) return;

    // Store original HTML for each li so we can restore highlights
    const allCards = Array.from(document.querySelectorAll('.commands-section .update-entry'));
    const footerNote = document.querySelector('.commands-footer-note');

    // Build index: each <li> in .update-list gets indexed
    const items = [];
    allCards.forEach(card => {
        const cardTitle = (card.querySelector('.update-date') || card.querySelector('h2') || {}).textContent || '';
        const lis = Array.from(card.querySelectorAll('.update-list > li'));
        lis.forEach(li => {
            items.push({
                li,
                card,
                cardTitle,
                originalHTML: li.innerHTML,
                searchText: li.textContent
            });
        });
    });

    function doSearch(raw) {
        const query = raw.trim();

        // Restore all original HTML first
        items.forEach(item => {
            item.li.innerHTML = item.originalHTML;
            item.li.classList.remove('cmd-hidden');
        });
        allCards.forEach(c => {
            c.classList.remove('cmd-card-hidden');
            // Re-open any code blocks that were opened
        });
        if (footerNote) footerNote.classList.remove('cmd-card-hidden');
        if (noResults) noResults.style.display = 'none';
        if (resultsEl) resultsEl.textContent = '';

        if (!query) {
            clearBtn.classList.remove('visible');
            initCodeToggles();
            return;
        }
        clearBtn.classList.add('visible');

        const THRESHOLD = 0.55;
        let visibleCount = 0;

        items.forEach(item => {
            const score = fuzzyScore(item.searchText, query);
            if (score >= THRESHOLD) {
                item.li.classList.remove('cmd-hidden');
                item.li.innerHTML = highlightText(item.originalHTML, query);
                visibleCount++;
            } else {
                item.li.classList.add('cmd-hidden');
            }
        });

        // Re-init code toggles — innerHTML replacement destroys their listeners
        initCodeToggles();

        // Hide cards where all items are hidden
        allCards.forEach(card => {
            const lis = card.querySelectorAll('.update-list > li');
            const allHidden = Array.from(lis).every(li => li.classList.contains('cmd-hidden'));
            if (allHidden) card.classList.add('cmd-card-hidden');
            else card.classList.remove('cmd-card-hidden');
        });

        if (footerNote) footerNote.classList.add('cmd-card-hidden');

        const lang = localStorage.getItem('language') || 'ru';
        if (visibleCount === 0) {
            if (noResults) noResults.style.display = 'block';
            if (noResultsText) noResultsText.textContent = lang === 'en' ? 'Nothing found' : 'Ничего не найдено';
        } else {
            if (resultsEl) {
                resultsEl.textContent = lang === 'en'
                    ? `Found: ${visibleCount} command${visibleCount !== 1 ? 's' : ''}`
                    : `Найдено: ${visibleCount} команд${visibleCount === 1 ? 'а' : visibleCount < 5 ? 'ы' : ''}`;
            }
        }
    }

    input.addEventListener('input', () => doSearch(input.value));
    clearBtn.addEventListener('click', () => {
        input.value = '';
        doSearch('');
        input.focus();
    });

    // Update placeholder & hint on language change — hook into setLanguage
    const origSetLanguage = window._setLanguageHook;
    window._searchReinit = function() {
        // Rebuild index after language swap (DOM rebuilt)
        const newItems = document.querySelectorAll('.commands-section .update-list > li');
        items.length = 0;
        document.querySelectorAll('.commands-section .update-entry').forEach(card => {
            const cardTitle = (card.querySelector('.update-date') || card.querySelector('h2') || {}).textContent || '';
            card.querySelectorAll('.update-list > li').forEach(li => {
                items.push({ li, card, cardTitle, originalHTML: li.innerHTML, searchText: li.textContent });
            });
        });
        const newInput = document.getElementById('cmd-search');
        if (newInput) {
            const lang = localStorage.getItem('language') || 'ru';
            newInput.placeholder = lang === 'en' ? 'Search command...' : 'Поиск команды...';
            newInput.addEventListener('input', () => doSearch(newInput.value));
            document.getElementById('cmd-search-clear')?.addEventListener('click', () => {
                newInput.value = '';
                doSearch('');
                newInput.focus();
            });
        }
    };
}


