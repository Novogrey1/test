// ============================================================
function initCodeToggles() {
    document.querySelectorAll('.code-toggle-btn').forEach(function (btn) {
        // Clone to remove old listeners
        var newBtn = btn.cloneNode(true);
        btn.parentNode.replaceChild(newBtn, btn);
        newBtn.addEventListener('click', function () {
            var wrap = newBtn.closest('.code-toggle-wrap');
            if (!wrap) return;
            var block = wrap.querySelector('.code-block');
            if (!block) return;
            var isOpen = block.classList.contains('open');
            if (isOpen) {
                block.classList.remove('open');
                newBtn.classList.remove('open');
                // restore arrow direction
                var arrow = newBtn.querySelector('.code-arrow');
                if (arrow) arrow.textContent = '▾';
            } else {
                block.classList.add('open');
                newBtn.classList.add('open');
                var arrow = newBtn.querySelector('.code-arrow');
                if (arrow) arrow.textContent = '▴';
            }
        });
    });
}

